# Que : Write a Python program to print a formatted string using print() and f-string.

# Define variables
name = "Alice"
age = 30
height = 5.6

# Print using print() with concatenation and type conversion
print("Name: " + name + ", Age: " + str(age) + ", Height: " + str(height) + " feet")

# Print using an f-string for formatting
print(f"Name: {name}, Age: {age}, Height: {height} feet")
