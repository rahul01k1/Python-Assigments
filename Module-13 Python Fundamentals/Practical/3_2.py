# Que : Practical Example 1: How does the Python code structure work?

# This is a Python program to calculate the area of a rectangle

# Step 1: Define variables
length = 10.5   # Length of the rectangle in units
width = 7.2     # Width of the rectangle in units

# Step 2: Calculate the area
area = length * width

# Step 3: Output the result
print("The area of the rectangle is:", area)

'''
How the code structure works:
1.Comments (#):
Notes or explanations embedded within the code to make it understandable. Comments are ignored by Python during execution.

2.Variable Declaration:
Variables (length, width, area) store data values. In Python, you don't need to declare data types explicitly; Python infers them automatically.

3.Calculations/Operations:
Lines like area = length * width perform calculations using operators like * for multiplication.

4.Output Statement:
The print() function displays the result on the console.



'''
