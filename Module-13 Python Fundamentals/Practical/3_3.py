#Que : Practical Example 2: How to create variables in Python?

# Integer variable
age = 25
print("Age:", age)
print("Type of age:", type(age))

# Float variable
height = 5.9
print("\nHeight:", height)
print("Type of height:", type(height))

# String variable
name = "Alice"
print("\nName:", name)
print("Type of name:", type(name))

# Boolean variable
is_student = True
print("\nIs Student:", is_student)
print("Type of is_student:", type(is_student))

# List variable
fruits = ["apple", "banana", "cherry"]
print("\nFruits:", fruits)
print("Type of fruits:", type(fruits))

# Tuple variable
dimensions = (1920, 1080)
print("\nDimensions:", dimensions)
print("Type of dimensions:", type(dimensions))

# Dictionary variable
person = {"name": "Bob", "age": 30}
print("\nPerson:", person)
print("Type of person:", type(person))

# Set variable
unique_numbers = {1, 2, 3, 3, 2}
print("\nUnique Numbers:", unique_numbers)
print("Type of unique_numbers:", type(unique_numbers))
