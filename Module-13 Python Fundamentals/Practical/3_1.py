#Que : Write a Python program to demonstrate the creation of variables and different data types.

# Integer variable
age = 25
print("Age:", age)
print("Type of age:", type(age))

# Float variable
height = 5.9
print("\nHeight:", height)
print("Type of height:", type(height))

# String variable
name = "Alice"
print("\nName:", name)
print("Type of name:", type(name))

# Boolean variable
is_student = True
print("\nIs Student:", is_student)
print("Type of is_student:", type(is_student))

# None Variable
userName = None # None Data Type
print("Type Of userName", type(userName))